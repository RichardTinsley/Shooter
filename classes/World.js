import { TILE_SIZE, COLUMNS, ROWS } from "../index.js";
import { PlacementTile } from './PlacementTile.js';

export class World {
    constructor(map){
        this.backgroundLayer = document.getElementById(map) // image.src = new Image() doesn't reload image from broswer memory
        this.placementTiles = this.initialiseTiles();
    }

    drawGrid(ctx){
        for (let row = 0; row < ROWS; row++)
            for (let column = 0; column < COLUMNS; column++)
                ctx.strokeRect(
                    column * TILE_SIZE,
                    row * TILE_SIZE,
                    TILE_SIZE,
                    TILE_SIZE
                );   
    }

    drawBackground(ctx){
        ctx.drawImage(this.backgroundLayer, 0, 0);
    }

    make2DArray(array){
        const array2D = [];
        for (let i = 0; i < array.length; i+= 40)
            array2D.push(array.slice(i, i + 40));
        return array2D
    }
    
    initialiseTiles() {
        const placementTilesData2D = this.make2DArray(placementTilesData);
        const placementTiles = [];
        placementTilesData2D.forEach((row, y) => {
            row.forEach((symbol, x) => {
                if (symbol != 0) 
                    placementTiles.push(
                        new PlacementTile({ 
                            position: { 
                                x: x * 32, 
                                y: y * 32 
                            } 
                        }));
            })
        })
        return placementTiles
    }
}

