import { TILE_SIZE } from "../index.js";

export class Level {
    constructor(){
        this.level1 = new Image();
        this.level1.src = "./images/LEVEL1.png";

        this.placementTiles = this.initialiseTiles();
    }

    make2DArray(array){
        const array2D = [];
        for (let i = 0; i < array.length; i+= 40)
            array2D.push(array.slice(i, i + 40));
        return array2D;
    }
    
    initialiseTiles(){
        const placementTilesData2D = this.make2DArray(placementTilesData);
        const placementTiles = [];
        placementTilesData2D.forEach((row, y) => {
            row.forEach((symbol, x) => {
                if (symbol != 0) 
                    placementTiles.push(
                        new PlacementTile({ 
                            position: { 
                                x: x * 32, 
                                y: y * 32 
                            } 
                        }));
            })
        })
        return placementTiles;
    }
    renderLevel(ctx){
        ctx.drawImage(this.level1, 0, 0);
        this.placementTiles.forEach((tile) => tile.renderTile(ctx));
    }
}

class PlacementTile {
    constructor({ 
        position = { 
            x: 0, 
            y: 0 
        } }) {
        this.position = position;
        this.size = TILE_SIZE;
        this.colour;
        this.occupied = false;
        this.mouseOver = false;
    }

    renderTile(ctx){
        if(this.mouseOver)
            this.colour = 'rgba(50, 255, 50, 0.15)';
        else
            this.colour = 'rgba(255, 255, 255, 0.15)';
        
        ctx.fillStyle = this.colour;
        ctx.fillRect(this.position.x, this.position.y, this.size, this.size);
    }
}

