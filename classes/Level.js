import { TILE_SIZE, COLUMNS, ROWS } from "../index.js";

export class Level {
    constructor(game){
        this.game = game;
        this.backgroundLayer = document.getElementById('LEVEL1') // image.src = new Image() doesn't reload image from broswer memory
        this.placementTiles = this.initialiseTiles();
    }

    make2DArray(array){
        const array2D = [];
        for (let i = 0; i < array.length; i+= 40)
            array2D.push(array.slice(i, i + 40));
        return array2D;
    }
    
    initialiseTiles(){
        const placementTilesData2D = this.make2DArray(placementTilesData);
        const placementTiles = [];
        placementTilesData2D.forEach((row, y) => {
            row.forEach((symbol, x) => {
                if (symbol != 0) 
                    placementTiles.push(
                        new PlacementTile({ 
                            position: { 
                                x: x * 32, 
                                y: y * 32 
                            } 
                        }));
            })
        })
        return placementTiles;
    }

    renderTiles(ctx){
        this.placementTiles.forEach((tile) => tile.update(ctx));
    }

    drawGrid(ctx){
        for (let row = 0; row < ROWS; row++)
            for (let column = 0; column < COLUMNS; column++)
                ctx.strokeRect(
                    column * TILE_SIZE,
                    row * TILE_SIZE,
                    TILE_SIZE,
                    TILE_SIZE
                );   
    }

    renderLevel(ctx){
        ctx.drawImage(this.backgroundLayer, 0, 0);
        this.renderTiles(ctx);
        if(this.game.debug) this.drawGrid(ctx);
    }
}

class PlacementTile {
    constructor({ 
        position = { 
            x: 0, 
            y: 0 
        } }) {
        this.position = position;
        this.size = TILE_SIZE;
        this.colour = 'rgba(255, 255, 255, 0.15)';
        this.occupied = false;
        this.mouseOver = false;
    }
    draw(ctx) {
        ctx.fillStyle = this.colour;
        ctx.fillRect(this.position.x, this.position.y, this.size, this.size);
    }
    update(ctx) {
        this.draw(ctx);
        if(this.mouseOver)
            this.colour = 'rgba(50, 255, 50, 0.15)';
        else
            this.colour = 'rgba(255, 255, 255, 0.15)';
    }
}

